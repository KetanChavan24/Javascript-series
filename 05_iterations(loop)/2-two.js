//WHILE LOOP

let heroes = ['Batman' , 'Superman' , 'Iron-man', 'Spider-man'];

let count = 0;
while (count <= heroes.length) {
    console.log(`I am ${heroes[count]}`);
    count ++;
}

//DO WHILE LOOP

let score = 11;
do{
    console.log(`${score}`);
    score++;
}while(score <= 10)
//the loop will run only once by giving the output: 11 and then terminate 
